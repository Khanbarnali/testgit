package Javapractice;

public class lec3forloop {

	public static void main(String[] args) {
		
		// for loop 
		// Print even numbers from 23 to 30.
		
		for(int f =23; f <= 30; f+=3) {
			System.out.println(+f);
			
		}
		for(int k=0;k<5;k++) {
			System.out.println("i am a student of nexthnext");
		}
	
		
		
	}

}
